function enrichTags(seed: Seed): PlayFilterTag[] {
  const tags = [...seed.tags];
  if (seed.difficulty === "轻松") tags.push("不想太累");
  if (outingIds.has(seed.id) || seed.bestTime.includes("一日")) tags.push("想有出门感");
  if (photoIds.has(seed.id) || seed.tags.includes("夜景") || seed.tags.includes("海边")) tags.push("想拍照");
  if (foodIds.has(seed.id) || seed.tags.includes("咖啡")) tags.push("想吃点好的");
  return Array.from(new Set(tags));
}

