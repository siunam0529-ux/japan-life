type Seed = Omit<PlayDestination, "afternoonPlan" | "budgetBreakdown" | "eveningPlan" | "image" | "morningPlan" | "planSteps" | "rainyPlan" | "tags" | "transportNote"> & {
  imageKey: keyof typeof images;
  tags: PlayFilterTag[];
  afternoonPlan?: string;
  budgetBreakdown?: string[];
  eveningPlan?: string;
  morningPlan?: string;
  planSteps?: PlayPlanStep[];
  rainyPlan?: string;
  transportNote?: string;
};

function destination(seed: Seed): PlayDestination {
  const tags = enrichTags(seed);
  return {
    ...seed,
    image: images[seed.imageKey],
    tags,
    planSteps: seed.planSteps ?? defaultSteps,
    morningPlan: seed.morningPlan ?? `${seed.name}适合先从车站周边开始，不用急着赶景点。`,
    afternoonPlan: seed.afternoonPlan ?? seed.reason,
    eveningPlan: seed.eveningPlan ?? "如果还有体力，可以在车站附近吃饭后回家。",
    rainyPlan: seed.rainyPlan ?? "下雨时减少户外时间，多安排咖啡店、商店街或室内休息点。",
    budgetBreakdown: seed.budgetBreakdown ?? ["交通：按出发地不同", "咖啡或小吃：500〜1200円", "吃饭：1000〜2500円"],
    transportNote: seed.transportNote ?? `${seed.area}位于${seed.prefecture}，出发前建议确认换乘和回程时间。`,
  };
}

