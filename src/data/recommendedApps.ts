function createLocalIcon(label: string, bg: string, fg: string) {
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="128" height="128" viewBox="0 0 128 128"><rect width="128" height="128" rx="30" fill="${bg}"/><circle cx="96" cy="30" r="18" fill="${fg}" fill-opacity=".12"/><circle cx="30" cy="96" r="22" fill="${fg}" fill-opacity=".10"/><text x="64" y="76" text-anchor="middle" font-family="Arial, Helvetica, sans-serif" font-size="${label.length > 1 ? 34 : 52}" font-weight="800" fill="${fg}">${label}</text></svg>`;
  return `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`;
}

function resolveAppIcon(app: Omit<RecommendedApp, "status">) {
  if (app.iconUrl) return app.iconUrl;

  const meta = appIconMeta[app.id] ?? {
    bg: "#ecfdf5",
    fg: "#047857",
    label: app.name.replace(/[^A-Za-z0-9]/g, "").slice(0, 2).toUpperCase() || "JL",
  };
  return createLocalIcon(meta.label, meta.bg, meta.fg);
}

